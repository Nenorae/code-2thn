// Class Matematika
class Matematika {
    // Method for addition
    public int tambah(int a, int b) {
        return a + b;
    }

    // Method for subtraction
    public int kurang(int a, int b) {
        return a - b;
    }

    // Method for multiplication
    public int kali(int a, int b) {
        return a * b;
    }
}