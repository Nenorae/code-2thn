// Contoh operasi aritmatika sederhana
const angka1 = 10;
const angka2 = 5;

// Penambahan
const hasilPenambahan = angka1 + angka2;
console.log(`Penambahan: ${angka1} + ${angka2} = ${hasilPenambahan}`);

// Pengurangan
const hasilPengurangan = angka1 - angka2;
console.log(`Pengurangan: ${angka1} - ${angka2} = ${hasilPengurangan}`);

// Perkalian
const hasilPerkalian = angka1 * angka2;
console.log(`Perkalian: ${angka1} * ${angka2} = ${hasilPerkalian}`);

// Pembagian
const hasilPembagian = angka1 / angka2;
console.log(`Pembagian: ${angka1} / ${angka2} = ${hasilPembagian}`);
